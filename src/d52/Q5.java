package d52;

public class Q5 {
    public static void main(String[] args) {
        // 目盛り（物差し）に使う文字のパターンを用意
        String majors = "+----";
        // 入力できる数字の最大制限（400未満までOK）
        int LIMIT = 400;

        // もし入力された数字が1つもなければ、何もせずにプログラムを終了する
        if (args.length == 0) return;

        int max = 0; // 一番大きい数字を覚えておく変数
        int i = 0;   // ループで使うカウンター

        // 入力された数字の個数と同じ大きさの「数字入れ（配列）」を作る
        int[] data = new int[args.length];

        // 【準備1】入力された文字を数字に変換してチェックする
        for (String s : args) {
            // 文字列を「整数（int）」に変換して配列に保存
            data[i] = Integer.parseInt(s);

            // 範囲外の数字（0以下、または400以上）があれば、失敗として終了する
            if (data[i] <= 0 || data[i] >= LIMIT) return;

            // 今までの最大値より大きい数字が来たら、最大値を更新する
            if (max < data[i]) {
                max = data[i];
            }
            i++; // 次の数字の処理へ
        }

        // 【準備2】一番大きい数字に合わせて、目盛り（物差し）の長さを計算する
        // （5で割って切り上げる計算式）
        int majormax = (max - 1) / 5 + 1;

        // --------------------------------------------------
        // 【表示1】上の「目盛り（物差し）」を描く
        // --------------------------------------------------
        System.out.print("       "); // 数字の表示スペース分、左側に空白を作る

        for (i = 0; i < majormax; i++) {
            // "+----" の5文字を、0,1,2,3,4,0,1,2... と順番に1文字ずつ並べて表示する
            System.out.print(majors.charAt(i % 5));
        }
        System.out.println(""); // 目盛りが終わったら改行

        // --------------------------------------------------
        // 【表示2】数字と「アスタリスク(*)のグラフ」を描く
        // --------------------------------------------------
        for (i = 0; i < data.length; i++) {
            // 数字を「4桁分の幅」できれいに揃えて「 : 」と一緒に表示
            System.out.printf("%4d : ", data[i]);

            // 5ごとに1個の '*' を表示する（あまりが出たら1個追加する計算）
            int n = (data[i] - 1) / 5 + 1;

            // 計算した個数ぶん '*' を横に並べる
            for (int j = 0; j < n; j++) {
                System.out.print('*');
            }

            System.out.println(""); // 1行描き終わったら改行
        }
    }
}